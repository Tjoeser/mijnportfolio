function dropdownlinks() {
    var x = document.getElementById("mobilelinks2");
    if (x.style.display === "block") {
        x.style.display = "none";
    } else {
        x.style.display = "block";
        x.style.position = "absolute";
        x.style.top = "30px";
    }
}